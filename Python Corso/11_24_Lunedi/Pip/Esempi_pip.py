# PIP è l'acronimo di "Pip Installs Packages" o "Preferred Installer Program",
# che è il sistema di gestione dei pacchetti utilizzato da Python per installare e gestire pacchetti software.
# PIP permette agli sviluppatori di Python di installare facilmente pacchetti e dipendenze da PyPI,
# il Python Package Index, che è un repository di software per il linguaggio di programmazione Python.

# Funzioni principali di PIP:
# - Installa pacchetti da PyPI, GitHub e altre fonti, con possibilità di scegliere la versione.
# - Risolve automaticamente le dipendenze tra pacchetti.
# - Permette di aggiornare o rimuovere pacchetti facilmente.
# - Supporta la gestione di versioni specifiche (upgrade/downgrade).

# - Si integra con ambienti virtuali (venv, virtualenv) per gestire progetti isolati.
